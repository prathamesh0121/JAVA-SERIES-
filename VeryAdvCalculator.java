public class VeryAdvCalculator extends AdvCalculator {

    public int Power(int a,int b)
    {
        return (int)Math.pow(a, b);
    }
    public int percentage(int a,int b)
    {
        return (a/100)*b;
    }
    public int squre(int a)
    {
        return a*a;
    }
    
}


