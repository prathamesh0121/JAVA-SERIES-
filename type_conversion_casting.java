public class type_conversion_casting {
    public static void main(String args[])
    {
        int a= 10;
        int b=a;

        int c=257;
        byte d =(byte)c;

        float p=5.6f;
        int s= (int)p;

        int n1=10;
        int n2=5;
        int n3=n1/n2;

        System.out.println(b);

         
        System.out.println(d);

        System.out.println(s);

        System.out.println(n3);


    }
}
